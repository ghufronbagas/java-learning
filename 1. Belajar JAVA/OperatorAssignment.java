public class OperatorAssignment { 
    public static void main(String[] args) { //77777777777777

        //Operator Assignment
        int a = 1;
        a = a + 5;
        a = a + 78;
        System.out.println("Nilai a = " + a);      //contoh yang rumit


        //Operator Assignment Jumlah
        int b = 1;
        b += 83;
        System.out.println("Nilai b = " + b);

        
        //Operator Assignment Pengurangan
        int c = 1;
        c -= 1;
        System.out.println("Nilai c = " + c);

        
        //Operator Assignment Perkalian
        int d = 1;
        d *= 50;
        System.out.println("Nilai d = " + d);

        
        //Operator Assignment Pembagian
        int e = 1;
        e /= 1;
        System.out.println("Nilai e = " + e);



    }
    
}
